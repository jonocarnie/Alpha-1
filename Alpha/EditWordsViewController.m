- (void)viewDidLoad
{
    [super viewDidLoad];
    
    NSLog(@"In the edit words controller now!");
    
    
    NSString *errorDesc = nil;
    NSPropertyListFormat format;
    
    NSString *wordsPlistPath;
    
    
    wordsPlistPath = [[NSBundle mainBundle] pathForResource:@"Words" ofType:@"plist"];
    
    
    NSData *plistXML = [[NSFileManager defaultManager] contentsAtPath:wordsPlistPath];
    NSDictionary *wordTemp = (NSDictionary *)[NSPropertyListSerialization propertyListFromData:plistXML mutabilityOption:NSPropertyListMutableContainersAndLeaves format:&format errorDescription:&errorDesc];
    
    if (!wordTemp) {
        NSLog(@"Error reading plist: %@, format: %d", errorDesc, format);
    } else {
        self.storedWords = [NSMutableArray arrayWithArray:[wordTemp objectForKey:@"Words"]];
        NSLog(@"Array contains the following: %@", self.storedWords);
    }

    
